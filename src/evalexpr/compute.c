#include "evalexpr.h"

int	c_add(int nb1, int nb2)
{
	return (nb1 + nb2);
}

int	c_sub(int nb1, int nb2)
{
	return (nb1 - nb2);
}

int	c_mul(int nb1, int nb2)
{
	return (nb1 * nb2);
}

int	c_div(int nb1, int nb2)
{
	return (nb1 / nb2);
}

int	c_mod(int nb1, int nb2)
{
	return (nb1 % nb2);
}